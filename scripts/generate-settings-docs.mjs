import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  generateSettingsDocs,
  SETTINGS_PREVIEW_INPUT,
} from "../lib/settingsDocsGenerator.mjs";
import { toJavaScriptLiteral } from "../lib/optionsSerialization.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const outputPath = path.resolve(__dirname, "../app/generated/settings-docs.ts");
const checkMode = process.argv.includes("--check");

function createModuleContent() {
  const docs = generateSettingsDocs();
  const inputLiteral = toJavaScriptLiteral(SETTINGS_PREVIEW_INPUT);

  return `/* eslint-disable */\n/* AUTO-GENERATED FILE. DO NOT EDIT. */\n/* Generated by scripts/generate-settings-docs.mjs */\n\nexport type GeneratedSettingDoc = {\n  key: string;\n  anchor: string;\n  title: string;\n  description: string;\n  typeSignature: string;\n  exampleSettings: string;\n  preview: string;\n};\n\nexport const SETTINGS_PREVIEW_INPUT = ${inputLiteral};\n\nexport const SETTINGS_PREVIEW_INPUT_CODE = ${JSON.stringify(inputLiteral)};\n\nexport const SETTINGS_DOCS: GeneratedSettingDoc[] = ${JSON.stringify(docs, null, 2)};\n`;
}

const generatedContent = createModuleContent();

if (checkMode) {
  if (!fs.existsSync(outputPath)) {
    console.error(`Missing generated docs file: ${outputPath}`);
    process.exit(1);
  }

  const currentContent = fs.readFileSync(outputPath, "utf8");

  if (currentContent !== generatedContent) {
    console.error("Generated docs are out of date. Run: yarn docs:generate");
    process.exit(1);
  }

  console.log("Generated docs are up to date.");
  process.exit(0);
}

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, generatedContent, "utf8");
console.log(`Generated settings docs: ${outputPath}`);
